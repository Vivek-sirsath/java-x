package examplePackage;

public class UnaryOperator {

	public static void main(String[] args) {
		
		int number = 5;
		
		System.out.println("Pre increment operator:- " + (++number));
		System.out.println("Pre decrement operator:- " + (--number));
		
		System.out.println("Post increment operator:- " + (number++));
		System.out.println("Post decrement operator:- " + (number--));

	}

}
