package examplePackage;

public class AssignmentOperator1 {

	public static void main(String[] args) {

		int a = 10;

		a += 9;
		System.out.println(a); // 10+9=19

		a -= 5;
		System.out.println(a); // 19-5=14

		a /= 2;
		System.out.println(a); // a= a/2 // 14/2=7

		a *= 10;
		System.out.println(a); // 7*10=70

	}

}
