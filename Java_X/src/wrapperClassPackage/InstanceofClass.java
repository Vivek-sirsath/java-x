
/*
 * Instanceof - Operator is used to test whether the object is an instance of specified class or not.
 * It is also known as type-comparison operator.
 * Instanceof operator in java is repre. by 'instanceof' keyword.
 * Instanceof - operator returns a boolean value
 * 
 * */
package wrapperClassPackage;

public class InstanceofClass {

	public static void main(String[] args) {
		
		InstanceofClass inst = new InstanceofClass();
		
		System.out.println(inst instanceof InstanceofClass);  // true

	}

}
